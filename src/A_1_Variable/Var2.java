package A_1_Variable;

public class Var2 { // 변수 선언과 초기화
    public static void main(String[] args) {
        int a ; // 변수 선언 int = 정수
        a = 10; // 변수 초기화 > 10을 a라는 그릇에 담는다. 초기화 = 선언한 변수에 처음으로 값을 대입해서 저장하는 것
                // 10을 20으로 바꾸면 a를 출력할 때 모두 20으로 나옴
                // = 은 오른쪽에 있는 값을 왼쪽에 저장한다는 뜻, 수학에서 이야기 하는 두값이 같다와는 다른 뜻이다.
                // 처음으로 값을 대입해서 저장하는 것을 변수 초기화라고 한다.


        System.out.println(a); // a에 담은 10을 불러낸다. // 즉 값을 한번에 바꾸기 쉬운게 변수!!!
    }
}

