package infJava1_1.A_4_Loop.ex;

// 자연수 출력 - for 문 버전

// 처음 10개의 자연수를 출력하는 프로그램을 작성해보세요.
// 이때, count라는 변수를 사용해야 합니다.

public class ForEx1 {
    public static void main(String[] args) {
        for(int count = 1; count <= 10; count++){
            System.out.println(count);
        }

        // 강사 풀이
        for(int count = 1; count <= 10; count++){
            System.out.println(count);
        }
    }
}
